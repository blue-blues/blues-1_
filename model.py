def _init_weights( module):
    if isinstance(module, nn.Linear):
        torch.nn.init.normal_(module.weight, mean=0.0, std=0.02)
        if module.bias is not None:
            torch.nn.init.zeros_(module.bias)
    elif isinstance(module, nn.Embedding):
        torch.nn.init.normal_(module.weight, mean=0.0, std=0.02)



class FlashAttention3(nn.Module):
    def __init__(self, embed_dim, num_heads, dropout=0.0):
        super(FlashAttention3, self).__init__()
        self.embed_dim = embed_dim
        self.num_heads = num_heads
        self.head_dim = embed_dim // num_heads

        assert (
            self.head_dim * num_heads == embed_dim
        ), "Embedding dimension must be divisible by the number of heads."

        self.scaling = self.head_dim ** -0.5
        self.dropout = nn.Dropout(dropout)

        self.qkv_proj = nn.Linear(embed_dim, 3 * embed_dim, bias=False)
        self.out_proj = nn.Linear(embed_dim, embed_dim, bias=False)

    def forward(self, x):
        batch_size, seq_len, _ = x.size()

        # Project input to query, key, value
        qkv = self.qkv_proj(x).reshape(batch_size, seq_len, 3, self.num_heads, self.head_dim)
        q, k, v = qkv.permute(2, 0, 3, 1, 4)  # Shape: (3, batch_size, num_heads, seq_len, head_dim)

        # Calculate attention scores using Flash Attention
        attn_output = F.scaled_dot_product_attention(q, k, v, attn_mask=None)

        # Reshape the output back
        attn_output = attn_output.permute(0, 2, 1, 3).reshape(batch_size, seq_len, self.embed_dim)
        output = self.out_proj(attn_output)
        return self.dropout(output)

